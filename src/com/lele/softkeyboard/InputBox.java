package com.lele.softkeyboard;

import android.view.View;

public class InputBox {

	private View inputLayout;
	private View inputView;
	private View inputText;
	public View getInputLayout() {
		return inputLayout;
	}
	public void setInputLayout(View inputLayout) {
		this.inputLayout = inputLayout;
	}
	public View getInputView() {
		return inputView;
	}
	public void setInputView(View inputView) {
		this.inputView = inputView;
	}
	public View getInputText() {
		return inputText;
	}
	public void setInputText(View inputText) {
		this.inputText = inputText;
	}
	
	
}
